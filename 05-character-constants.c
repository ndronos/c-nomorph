# include <stdio.h>

int main() {

    printf("\n  print backslash:  \\");
    printf("\n  print tab:        \t    here");
    printf("\n  print vert tab:   \v    here");

    printf("\n  print return      \r   there");
    

    printf("\n  print question mark: \?     ");
    printf("\n  print double quote:  \"     ");


    printf("\n");

    return 0;

}

