#include <stdio.h>

int main(){

    

    x++;  //increment x. Evaluates to x.
    ++x;  //increment x. Evaluates to x+1.

    x--;  //decrement x. Evaluates to x.
    --x;  //decrement x. Evaluates to x-1.


    x+=3; //add 3 to x.
    x-=7; //subtract 7 from x.
    x*=2; //multiply x by 2.
    x/=2; //divide x by 2.
    x%=2; //replace x with modulo.


}
